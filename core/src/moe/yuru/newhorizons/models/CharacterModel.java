package moe.yuru.newhorizons.models;

public class CharacterModel {

    private String id;
    private String lastName;
    private String firstName;

    private CharacterModel() {
    }

    public String getId() {
        return id;
    }

    public String getLastName() {
        return lastName;
    }

    public String getFirstName() {
        return firstName;
    }

}
